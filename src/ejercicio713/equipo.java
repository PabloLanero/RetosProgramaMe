package ejercicio713;

public class equipo {
    int puntos;
    int gf;
    int gc;
    
    
    public equipo(int puntos, int gf, int gc) {
        this.puntos = puntos;
        this.gf = gf;
        this.gc = gc;
    }


    public int getPuntos() {
        return puntos;
    }


    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }


    public int getGf() {
        return gf;
    }


    public void setGf(int gf) {
        this.gf = gf;
    }


    public int getGc() {
        return gc;
    }


    public void setGc(int gc) {
        this.gc = gc;
    }

    


}
